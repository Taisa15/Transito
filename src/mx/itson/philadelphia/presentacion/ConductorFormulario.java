    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        String nombre = txtNombre.getText();
        String numeroLicencia = txtNumeroLicencia.getText();
        Date fechaAlta = dchFecha.getDate();
        
        if (ConductorDAO.guardar(nombre, numeroLicencia, fechaAlta)) {
            JOptionPane.showMessageDialog(this, "El registro se guard√≥ correctamente", "Registro guardado", JOptionPane.INFORMATION_MESSAGE);
            ConductorListado cl = new ConductorListado();
            cl.setVisible(true);
            hide();
        } else {
            JOptionPane.showMessageDialog(this, "No es posible guardar el registro.", "Registro no guardado", JOptionPane.ERROR_MESSAGE);
        }
        
        limpiarCampos();
        
        
        
    }//GEN-LAST:event_btnGuardarActionPerformed

